package com.example.sleepmood;

public class CalendarData {

    String start;
    String title;
    String description;
    String end;
    String email;

    public CalendarData(String start, String title, String description, String end, String email) {
        this.start = start;
        this.title = title;
        this.description = description;
        this.end = end;
        this.email = email;
    }
}
